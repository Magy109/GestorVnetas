    private void btn_iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_iniciarActionPerformed
      String busqueda_usuario= metod.buscarusuarioRegistrado(lusername.getText(),lpassword.getText());
       if(lusername.getText().equals("root") && lpassword.getText().equals("root")){
           JOptionPane.showMessageDialog(this,"Bienvenido Administrador");
            Personalizacion p= new Personalizacion();
           p.setVisible(true);
           this.dispose();
       }else if(busqueda_usuario.equals("usuario encontrado")){
           String busqueda_nombre=metod.buscarNombre(lusername.getText());
           JOptionPane.showMessageDialog(this,"Bienvenid@ \n " +busqueda_nombre);
           Personalizacion p= new Personalizacion();
           p.setVisible(true);
           this.dispose();
       }else{
           JOptionPane.showMessageDialog(this,"Usuario y/o contrase√±a incorrectos");
           lusername.setText("");
           lpassword.setText("");
       }
    }//GEN-LAST:event_btn_iniciarActionPerformed

